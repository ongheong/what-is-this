import { useState } from 'react';
import { sculptureList } from './data.js';

export default function Gallery() {
  const [index, setIndex] = useState(0);
  const [showMore, setShowMore] = useState(false);

  let hasPrev = index > 0; //hasPrev boolean 변수로 index가 0보다 큰지 확인
  let hasNext = index < sculptureList.length - 1; //hasNext 변수로 index가 리스트의 길이보다 작은지 확인

  function handlePrevClick() {
    if (hasPrev) { //index가 0보다 클 때
      setIndex(index - 1);
    }
  }

  function handleNextClick() {
    if (hasNext) { //index가 리스트의 길이보다 작을 때
      setIndex(index + 1);
    }
  }

  function handleMoreClick() {
    setShowMore(!showMore);
  }

  let sculpture = sculptureList[index];
  return (
    <>
      <button
        onClick={handlePrevClick}
        disabled={!hasPrev} //hasPrev가 false일 때 비활성화
      >
        Previous
      </button>
      <button
        onClick={handleNextClick}
        disabled={!hasNext} //hasNext가 false일 때 비활성화
      >
        Next
      </button>
      <h2>
        <i>{sculpture.name} </i> 
        by {sculpture.artist}
      </h2>
      <h3>  
        ({index + 1} of {sculptureList.length})
      </h3>
      <button onClick={handleMoreClick}>
        {showMore ? 'Hide' : 'Show'} details
      </button>
      {showMore && <p>{sculpture.description}</p>}
      <img 
        src={sculpture.url} 
        alt={sculpture.alt}
      />
    </>
  );
}


